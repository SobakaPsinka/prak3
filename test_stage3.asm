# Полная тестовая программа для Варианта 21 (ИСПРАВЛЕННАЯ)
# Этапы 1-2: Базовые операции
LOAD 25 0
LOAD 100 1
LOAD 625 2
LOAD 959 3    # Регистр для адреса 959

WRITE 0 0 1     # memory[100] = 25
WRITE 1 50 1    # memory[150] = 100
WRITE 2 0 3     # memory[959] = 625 (адрес из reg[3])

# Этап 3: Команда SQRT
SQRT 100 200    # sqrt(25) -> memory[200] = 5
SQRT 150 201    # sqrt(100) -> memory[201] = 10
SQRT 959 396    # sqrt(625) -> memory[396] = 25